const select = document.querySelector("#country");

const but = document.querySelector("#but")

const names = document.querySelector("#name");

const img = document.querySelector("#flag")

const output = document.querySelector("#output");

for(let count in countries){
    let option = document.createElement("option");
    option.innerText = count;
    option.value = countries[count];
    if(option.value === "global"){
        option.selected = selected;
    }
    select.append(option);

}
let URL;
but.addEventListener("click" , ()=>{
    let selected = select.value;
    let inname = names.value;
    console.log(inname);
    console.log(selected);
    if(selected == "global"){
         URL = `https://api.agify.io?name=${inname}`;
    }
    else{
         URL = `https://api.agify.io?name=${inname}&country_id=${selected}`;
    }
    
    console.log(URL)
    let update = async ()=>{
        let raw = await fetch(URL);
        let data = await raw.json();
        console.log(data);
        if(data.age === null){
            output.innerHTML = `<b>Ans</b>:From the data of ${data.count} people with the same name , Your age might be 0 years.`
        }
        else{
            output.innerHTML = `<b>Ans</b>:From the data of ${data.count} people with the same name , Your age might be ${data.age} years.`
        }
    }
    update();
})


select.addEventListener("change" , ()=>{
    let selected = select.value;
    console.log(img)

    if(selected === "global"){
        img.setAttribute("src" , "earth.png");
    }
    else{
        let url = `https://flagsapi.com/${selected}/flat/64.png`;
        img.setAttribute("src" , url);
    }
})

